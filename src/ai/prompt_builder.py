def new_product_prompt(name: str, category: str, features: str, audience: str) -> str:
    """Промпт для создания новой карточки товара."""
    return (
        "Создай продающую карточку товара для маркетплейса по следующим данным:\n\n"
        f"Название товара: {name}\n"
        f"Категория: {category}\n"
        f"Особенности товара: {features}\n"
        f"Целевая аудитория: {audience}\n\n"
        "Ответ верни строго в заданном формате:\n"
        "🏷 Название / 📝 Описание / ⭐ Преимущества / 🔍 SEO-ключи / 📦 Характеристики"
    )


def improve_product_prompt(existing_text: str) -> str:
    """Промпт для улучшения существующего описания товара."""
    return (
        "Улучши существующее описание товара: сделай текст более продающим, "
        "улучши структуру и читаемость, добавь релевантные SEO-ключевые слова, "
        "дополни характеристики если возможно.\n\n"
        f"Исходный текст:\n{existing_text}\n\n"
        "Ответ верни строго в заданном формате:\n"
        "🏷 Название / 📝 Описание / ⭐ Преимущества / 🔍 SEO-ключи / 📦 Характеристики"
    )
